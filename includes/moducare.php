<p align="left"><font face="Arial, Helvetica, sans-serif" size="2" color="#006633"><b><br>
  Why is the &quot;sterolin&quot; such an important part of the Moducare&reg; 
  formulation?</b></font><font face="Arial, Helvetica, sans-serif" size="2"><br>
  In nature, plants never contain sterols only. The sterols are always associated 
  with their glucoside sterolin &#150; a glucose molecule attached to the sterol 
